#include <stdio.h>

/**
 * add - addition fxn
 * @a: variable a
 * @b: variable b
 * Return: add
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - subtraction fxn
 * @a: variable a
 * @b: variable b
 * Return: a - b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multilpication fxn
 * @a: variable a
 * @b: variable b
 * Return: a * b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - division fxn
 * @a: variable a
 * @b: variable b
 * Return: a / b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - modulus fxn
 * @a: variable a
 * @b: variable b
 * Return: a % b
 */

int mod(int a, int b)
{
return (a % b);
}
